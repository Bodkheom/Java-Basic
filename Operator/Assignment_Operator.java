public class Assignment_Operator {
    public static void main(String[] args) {
        int a=4;
        a+=10; // a=a+10
        System.out.println("Value of a after adding 10 is: " + a);

        int b=5;
        b-=2;  // b=b-2
        System.out.println("Value of b after subtracting 2 is: " + b);
        
        int c=10;
        c*=2;  // c=c*2
        System.out.println("Value of c after multiplying by 2 is: " + c);
        
        int d=4;
        d/=2;  // d=d/2
        System.out.println("Value of d after Divide by 2 is " + d);
    }
    
}
